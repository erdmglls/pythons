#Atama Oeratörleri

# = (basit atama operatörü)
x=10
print("x =", x)

# += (toplayarak atama)
x+=5   # x = x + 5
print("x += 5 →", x)

# -= (çıkararak atama)
x-=3   # x = x - 3
print("x -= 3 →", x)

# *= (çarparak atama)
x*=2   # x = x * 2
print("x *= 2 →", x)

# /= (bölerek atama)
x/=4   # x = x / 4
print("x /= 4 →", x)

# //= (tamsayı bölme ile atama)
x//=2  # x = x // 2
print("x //= 2 →", x)

# %= (mod, yani kalan alarak atama)
x%=3   # x = x % 3
print("x %= 3 →", x)

# **= (üs alarak atama)
x=4
x **= 2  # x = x ** 2
print("x **= 2 →", x)
